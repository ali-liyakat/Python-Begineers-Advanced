# volume of cylinder

def volume_cylinder(radius, height=5):
    '''This function takes the radius and height of a cylinder and returns the volume'''
    volume = 3.14 * radius**2 * height
    return volume


# volume of triangle

def volume_triangle(base, height):
    '''This function takes the base and height of a triangle and returns the volume'''
    volume = 0.5 * base * height
    return volume

